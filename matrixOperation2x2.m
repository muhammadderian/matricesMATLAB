% Matrix 2x2 abstract value
A = [ 10 9;8 7 ]
B = [ 6 5;4 3 ]

% Addition
disp("Matrices A + B");
A+B
% Subtract
disp("Matrices A - B");
A-B
% Devide
disp("Matrices A / B");
A/B
% Power(square)
r = input('Input the power = ');
disp('Matrices A');
A^r
disp('Matrices B');
B^r
% Multiply
disp("Matrices A x B");
A*B
% Multiply Element
disp("Matrices A x B Element");
A.*B
% Multiply Scalar
Number = input('Input Nember for Multiply to Matrices = ');
disp('Matrices A');
A*Number
disp('Matrices B');
B*Number