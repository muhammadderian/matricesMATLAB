%perbandingan angka terbesar dari 2 buah angka dengan manggunakan
%algoritma percabangan dimana kedua angka tersebut inputan oleh user

A = input('silahkan input bilangan A : ');
B = input('silahkan input bilangan B : ');

if (A>B)
    disp('Bilangan A lebih besar dari pada B');
elseif(A<B)
    disp('Bilangan B lebih besar dari pada A');
else
    disp('Bilangan A sama dengan B');
end